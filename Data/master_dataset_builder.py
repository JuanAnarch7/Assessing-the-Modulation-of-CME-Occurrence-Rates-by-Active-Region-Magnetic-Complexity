"""
=============================================================================
Master Dataset Builder — Monthly Resolution
=============================================================================
Combines three data sources into a single monthly CSV:

  1. Filtered CMEs      → datos_procesados_2026_02_28.csv
  2. Sunspot numbers     → SN_m_tot_V2.0 (1).txt
  3. AR Features         → ar_features_monthly.csv  (generated by ar_features_builder.py)

Columns of the resulting dataset:
  yearmonth                - Period (YYYY-MM)
  sunspot_number            - Sunspot number (SIDC v2)
  count_cmes_all             - Total CMEs for the month (no filter)
  count_cmes_normal          - CMEs with width between 20° and 120° (narrow/normal)
  count_cmes_partial_halo    - CMEs with width between 120° and <360° (partial halo)
  count_cmes_halo            - CMEs with width == 360° (full halo)
  count_cmes_slow             - CMEs with speed 0–600 km/s
  count_cmes_moderate         - CMEs with speed 600–1000 km/s
  count_cmes_fast              - CMEs with speed 1000–1500 km/s
  count_cmes_extreme           - CMEs with speed > 1500 km/s
  count_cmes_filtered         - CMEs meeting the original speed AND width filter
  n_total                    - Unique active regions in the month
  n_high_complexity          - ARs with peak complexity >= 4 (βγ, βγδ, γ, γδ)
  frac_high_complexity        - n_high_complexity / n_total
  complexity_area_sum        - Sum of complexity × area (all daily observations)
  n_large                    - Long-lived ARs (>= 7 days)
  n_small                     - Mid-lived ARs (2–6 days)
  n_ephemeral                 - Ephemeral ARs (<= 1 day)
  frac_large                 - n_large / n_total
  frac_small                  - n_small / n_total
  frac_ephemeral               - n_ephemeral / n_total

CME filtering parameters (adjust as needed):
  MIN_SPEED  = 750 km/s
  MIN_WIDTH  = 120°
  MAX_WIDTH  = 360°
  YEAR_START = 1996
  YEAR_END   = 2026
=============================================================================
"""

import csv
import warnings

import numpy as np
import pandas as pd

warnings.filterwarnings("ignore")

# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

# Input files
CME_FILE     = "datos_procesados_2026_02_28.csv"
SUNSPOT_FILE = "SN_m_tot_V2.0 (1).txt"
AR_FILE      = "ar_features_monthly.csv"

# Output file
OUTPUT_FILE  = "master_monthly.csv"

# Date range (the dataset ends in February 2026)
YEAR_START = 1996
YEAR_END   = 2026
END_MONTH  = "2026-02"   # final cutoff month for the master dataset

# CME filters
MIN_SPEED = 750
MIN_WIDTH = 120
MAX_WIDTH = 360

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def make_full_range(year_start: int, year_end: int) -> pd.Index:
    """Generates the full monthly index as 'YYYY-MM' strings."""
    return (
        pd.period_range(start=f"{year_start}-01", end=f"{year_end}-12", freq="M")
        .astype(str)
    )


def print_section(title: str) -> None:
    print(f"\n{'=' * 65}")
    print(f"  {title}")
    print(f"{'=' * 65}")


# ---------------------------------------------------------------------------
# 1. Sunspot numbers (SIDC SN v2)
# ---------------------------------------------------------------------------

def load_sunspots(path: str, year_start: int, year_end: int) -> pd.DataFrame:
    """
    Reads the SIDC text file (whitespace-separated columns).
    Relevant columns: [0]=Year, [1]=Month, [3]=SunspotNumber
    """
    print_section("1. Loading sunspot numbers")

    df = pd.read_csv(
        path,
        sep=r"\s+",
        header=None,
        usecols=[0, 1, 3],
        names=["year", "month", "sunspot_number"],
    )
    df["year"]  = df["year"].astype(int)
    df["month"] = df["month"].astype(int)
    df["yearmonth"] = (
        pd.to_datetime(df[["year", "month"]].assign(day=1))
        .dt.to_period("M")
        .astype(str)
    )

    df = df[
        (df["year"] >= year_start) &
        (df["year"] <= year_end)
    ][["yearmonth", "sunspot_number"]].copy()

    # Replace -1 values (SIDC missing-data marker) with NaN
    df["sunspot_number"] = df["sunspot_number"].replace(-1, np.nan)

    print(f"  Range: {df['yearmonth'].min()} -> {df['yearmonth'].max()}")
    print(f"  Observations: {len(df)}")
    return df


# ---------------------------------------------------------------------------
# 2. CMEs
# ---------------------------------------------------------------------------

def load_cmes(path: str, year_start: int, year_end: int,
              min_speed: float, min_width: float, max_width: float) -> pd.DataFrame:
    """
    Loads the CME dataset and builds monthly counts with the following
    width and speed categories:

    By width type:
      count_cmes_all          : all CMEs within the date range
      count_cmes_normal       : width between 20° and 120° (narrow/normal CMEs)
      count_cmes_partial_halo : width between 120° and <360° (partial halo)
      count_cmes_halo         : width == 360° (full halo)

    By speed (all CMEs, no width filter):
      count_cmes_slow         : 0   – 600  km/s
      count_cmes_moderate     : 600 – 1000 km/s
      count_cmes_fast         : 1000– 1500 km/s
      count_cmes_extreme      : > 1500 km/s

    Combined (original filter, kept for backward compatibility):
      count_cmes_filtered     : speed >= min_speed AND min_width <= width <= max_width
    """
    print_section("2. Loading and filtering CMEs")

    df = pd.read_csv(path, low_memory=False)
    df["Fecha"] = pd.to_datetime(df["Fecha"], errors="coerce")
    df[["Central", "Ancho", "Rapidez"]] = df[["Central", "Ancho", "Rapidez"]].apply(
        pd.to_numeric, errors="coerce"
    )
    df["year"] = df["Fecha"].dt.year
    df["yearmonth"] = df["Fecha"].dt.to_period("M").astype(str)

    # Date-range filter
    df = df[(df["year"] >= year_start) & (df["year"] <= year_end)]

    full_idx = make_full_range(year_start, year_end)

    def monthly_count(mask: pd.Series, col_name: str) -> pd.Series:
        return (
            df[mask]
            .groupby("yearmonth")
            .size()
            .reindex(full_idx, fill_value=0)
            .rename(col_name)
        )

    # ── Width masks ──────────────────────────────────────────────────────
    mask_normal       = (df["Ancho"] >=  20) & (df["Ancho"] <= 120)
    mask_partial_halo = (df["Ancho"] >  120) & (df["Ancho"] <  360)
    mask_halo         =  df["Ancho"] == 360

    # ── Speed masks ──────────────────────────────────────────────────────
    v = df["Rapidez"]
    mask_slow     = (v >=    0) & (v <   600)
    mask_moderate = (v >=  600) & (v <  1000)
    mask_fast_v   = (v >= 1000) & (v <  1500)
    mask_extreme  =  v >= 1500

    # ── Original combined filter (backward compatibility) ─────────────────
    mask_filtered = (
        (df["Rapidez"] >= min_speed) &
        (df["Ancho"]   >= min_width) &
        (df["Ancho"]   <= max_width)
    )

    counts = pd.concat(
        [
            # Total
            monthly_count(pd.Series([True] * len(df), index=df.index), "count_cmes_all"),

            # By width type
            monthly_count(mask_normal,       "count_cmes_normal"),
            monthly_count(mask_partial_halo, "count_cmes_partial_halo"),
            monthly_count(mask_halo,         "count_cmes_halo"),

            # By speed
            monthly_count(mask_slow,     "count_cmes_slow"),
            monthly_count(mask_moderate, "count_cmes_moderate"),
            monthly_count(mask_fast_v,   "count_cmes_fast"),
            monthly_count(mask_extreme,  "count_cmes_extreme"),

            # Original combined filter
            monthly_count(mask_filtered, "count_cmes_filtered"),
        ],
        axis=1,
    ).reset_index().rename(columns={"index": "yearmonth"})

    n = len(df)
    print(f"  Total CMEs (date range)        : {n}")
    print(f"  -- By width -------------------------------------------")
    print(f"  Normal        (20°-120°)       : {int(counts['count_cmes_normal'].sum())}")
    print(f"  Partial halo  (120°-<360°)     : {int(counts['count_cmes_partial_halo'].sum())}")
    print(f"  Full halo     (360°)           : {int(counts['count_cmes_halo'].sum())}")
    print(f"  -- By speed -------------------------------------------")
    print(f"  Slow          (0-600 km/s)     : {int(counts['count_cmes_slow'].sum())}")
    print(f"  Moderate      (600-1000 km/s)  : {int(counts['count_cmes_moderate'].sum())}")
    print(f"  Fast          (1000-1500 km/s) : {int(counts['count_cmes_fast'].sum())}")
    print(f"  Extreme       (>1500 km/s)     : {int(counts['count_cmes_extreme'].sum())}")
    print(f"  -- Original combined filter ----------------------------")
    print(f"  Filtered      (v>={min_speed}, {min_width}°-{max_width}°): {int(counts['count_cmes_filtered'].sum())}")
    return counts


# ---------------------------------------------------------------------------
# 3. AR Features
# ---------------------------------------------------------------------------

def load_ar_features(path: str) -> pd.DataFrame:
    """
    Loads the AR features CSV generated by ar_features_builder.py.
    Renames the period column to 'yearmonth' if needed for consistency.
    """
    print_section("3. Loading AR Features")

    df = pd.read_csv(path)

    # Normalize the period column name
    if "yearmonth" not in df.columns:
        # Try to detect the period column
        period_col = [c for c in df.columns if "month" in c.lower() or "period" in c.lower()]
        if period_col:
            df = df.rename(columns={period_col[0]: "yearmonth"})

    # Ensure 'YYYY-MM' string format
    df["yearmonth"] = df["yearmonth"].astype(str).str[:7]

    print(f"  Range: {df['yearmonth'].min()} -> {df['yearmonth'].max()}")
    print(f"  Observations: {len(df)}")
    print(f"  Columns: {[c for c in df.columns if c != 'yearmonth']}")
    return df


# ---------------------------------------------------------------------------
# 4. Merge & validation
# ---------------------------------------------------------------------------

def build_master(
    df_sn:  pd.DataFrame,
    df_cme: pd.DataFrame,
    df_ar:  pd.DataFrame,
    year_start: int,
    year_end: int,
) -> pd.DataFrame:
    """
    Joins the three sources on the full monthly index.
    Months missing data in a given source are left as NaN (not dropped).
    """
    print_section("4. Building master dataset")

    full_idx = pd.DataFrame({"yearmonth": make_full_range(year_start, year_end)})

    master = (
        full_idx
        .merge(df_sn,  on="yearmonth", how="left")
        .merge(df_cme, on="yearmonth", how="left")
        .merge(df_ar,  on="yearmonth", how="left")
    )

    # Fill CME counts with 0 where there were no events (not NaN)
    cme_cols = ["count_cmes_all", "count_cmes_fast", "count_cmes_halo", "count_cmes_filtered"]
    master[cme_cols] = master[cme_cols].fillna(0).astype(int)

    master = master.sort_values("yearmonth").reset_index(drop=True)

    # Trim to the configured cutoff month
    master = master[master["yearmonth"] <= END_MONTH].reset_index(drop=True)

    return master


def print_diagnostics(master: pd.DataFrame) -> None:
    """Prints statistics and checks the internal consistency of the dataset."""
    print_section("5. Master dataset diagnostics")

    print(f"\n  Total rows     : {len(master)}")
    print(f"  Columns        : {len(master.columns)}")
    print(f"  Date range     : {master['yearmonth'].min()} -> {master['yearmonth'].max()}")

    print("\n  NaN values per column:")
    nans = master.isna().sum()
    for col, n in nans[nans > 0].items():
        print(f"    {col:<30s}: {n:4d}  ({100*n/len(master):.1f}%)")

    # Check fraction consistency
    print("\n  Fraction range check [0, 1]:")
    frac_cols = [c for c in master.columns if c.startswith("frac_")]
    for col in frac_cols:
        bad = master[(master[col] < 0) | (master[col] > 1)].dropna(subset=[col])
        status = "OK" if bad.empty else f"WARNING: {len(bad)} rows out of range"
        print(f"    {col:<30s}: {status}")

    # Check class sum consistency
    if all(c in master.columns for c in ["n_large", "n_small", "n_ephemeral", "n_total"]):
        check = (master["n_large"] + master["n_small"] + master["n_ephemeral"]) - master["n_total"]
        bad = check[check.abs() > 0].dropna()
        if bad.empty:
            print("\n  OK: n_large + n_small + n_ephemeral == n_total for every month")
        else:
            print(f"\n  WARNING: class-sum inconsistency in {len(bad)} months")

    print("\n  First 6 rows:")
    print(master.head(6).to_string(index=False))

    print("\n  Descriptive statistics:")
    num_cols = master.select_dtypes(include=[np.number]).columns.tolist()
    print(master[num_cols].describe().round(3).to_string())


# ---------------------------------------------------------------------------
# Main pipeline
# ---------------------------------------------------------------------------

if __name__ == "__main__":

    # Load sources
    df_sn  = load_sunspots(SUNSPOT_FILE, YEAR_START, YEAR_END)
    df_cme = load_cmes(CME_FILE, YEAR_START, YEAR_END,
                       MIN_SPEED, MIN_WIDTH, MAX_WIDTH)
    df_ar  = load_ar_features(AR_FILE)

    # Build master dataset
    master = build_master(df_sn, df_cme, df_ar, YEAR_START, YEAR_END)

    # Diagnostics
    print_diagnostics(master)

    # Save
    master.to_csv(
        OUTPUT_FILE,
        index=False,
        quoting=csv.QUOTE_NONNUMERIC,
        float_format="%.4f",
    )

    print_section("Saved")
    print(f"\n  master_monthly.csv  ->  {len(master)} rows x {len(master.columns)} columns")
